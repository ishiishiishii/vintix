def main():
    parser = argparse.ArgumentParser(description="Transfer learning from Minicheetah to Go2")
    parser.add_argument("-e", "--exp_name", type=str, default="minicheetah_to_go2_finetuning")
    parser.add_argument("-B", "--num_envs", type=int, default=4096)
    parser.add_argument("--max_iterations", type=int, default=21)  # 転移学習用に少し多め
    parser.add_argument("--pretrained_path", type=str, default="logs/minicheetah-walking/model_100.pt", 
                       help="Path to Minicheetah pretrained model for transfer learning")
    args = parser.parse_args()

    gs.init(logging_level="warning")

    log_dir = f"logs/{args.exp_name}"
    env_cfg, obs_cfg, reward_cfg, command_cfg = get_cfgs()
    train_cfg = get_train_cfg(args.exp_name, args.max_iterations)

    # 既存のログディレクトリを削除
    if os.path.exists(log_dir):
        shutil.rmtree(log_dir)
    os.makedirs(log_dir, exist_ok=True)

    # 設定を保存
    pickle.dump(
        [env_cfg, obs_cfg, reward_cfg, command_cfg, train_cfg],
        open(f"{log_dir}/cfgs.pkl", "wb"),
    )

    # Go2環境を作成
    env = Go2Env(
        num_envs=args.num_envs, env_cfg=env_cfg, obs_cfg=obs_cfg, reward_cfg=reward_cfg, command_cfg=command_cfg
    )

    runner = OnPolicyRunner(env, train_cfg, log_dir, device=gs.device)

    # Minicheetahで学習したモデルを読み込み
    print(f"Loading pretrained Minicheetah model from: {args.pretrained_path}")
    runner.load(args.pretrained_path)

    print("Starting transfer learning from Minicheetah to Go2...")
    runner.learn(num_learning_iterations=args.max_iterations, init_at_random_ep_len=True)
    
    print(f"Transfer learning completed! Model saved to: {log_dir}")


if __name__ == "__main__":
    main()

